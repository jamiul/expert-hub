                                </h3>
                                <div id="flush-collapseTwo" class="accordion-collapse collapse" aria-labelledby="flush-headingTwo" data-bs-parent="#accordionFlushExample">
                                <div class="accordion-body">
                                <p>This course has been designed to familiarize participants with the potential of using the
                                multivariate analysis method PLS-SEM in their research. The objectives of this course
                                are to provide (1) an in-depth methodological introduction to the PLS-SEM approach (the
                                nature of causal modeling, analytical objectives, and some statistics), (2) the
                                evaluation of measurement results, and (3) the evaluation of the structural model
                                results. More specifically, participants will understand the following topics:</p>
                            <ul>
                                <li>Fundamentals of latent variable modeling and model development</li>
                                <li>An introduction to PLS-SEM and its characteristics</li>
                                <li>When (not) to use PLS-SEM</li>
                                <li>PLS path model estimation using the standard, and weighted PLS (WPLS) algorithms
                                </li>
                                <li>Assessment and reporting of measurement and structural model results including
                                    bootstrapping</li>
                                <li>Prediction-oriented results analysis including PLSpredict</li>
                                <li>New criteria for model assessment such as HTMT for discriminant validity,
                                    goodness-of-fit indices (e.g., SRMR), and metrics for model comparisons (e.g., BIC)
                                </li>
                                <li>Mediation analysis</li>
                                <li>Outlook on advanced techniques</li>
                            </ul>
                            <p>Participants will receive a certificate of attendance, issued by the EduExHub.</p>
